void main(List<String> args) {
  //FOR LOOP

  for (var i = 1; i <= 5; i++) {
    print("Hello");
  }

  //FOR IN LOOP

  var List = [10, 20, 30];
  for (var x in List) {
    print(x);
  }

  //WHILE LOOP

  var i = 0;
  while (i <= 3) {
    i++;
    print(i);
  }

  //DO WHILE

  var j = 0;
  do {
    j++;
    print(j);
  } while (j <= 5);
}
